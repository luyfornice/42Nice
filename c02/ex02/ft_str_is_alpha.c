int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] > 'z' || ((str[i] > 'Z') && (str[i] < 'a')) || str[i] < 'A')
		{
			return (0);
		}
		else
		{
			i++;
		}
	}
	return (1);
}
